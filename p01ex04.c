#include <stdio.h>

int main()
{
	int x = 17528;
	int y = 1482;

	printf("%d * %d = %8d\n", x, y, x*y);
	printf("%d / %d = %8d\n", x, y, x/y);
	printf("%d + %d = %8d\n", x, y, x+y);
	printf("%d - %d = %8d\n", x, y, x-y);
	printf("%d %% %d = %8d\n", x, y, x%y);

	return 0;
}

