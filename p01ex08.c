#include <stdio.h>

int main()
{
	double upper, lower, height;
	printf("台形の「上底」「下底」「高さ」を入力-->");
	scanf("%lf %lf %lf", &upper, &lower, &height);
	printf("面積は %.3f\n", (upper + lower) * height / 2);
	return 0;
}

