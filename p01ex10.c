#include <stdio.h>

int main()
{
	int num;
	printf("整数を入力-->");
	scanf("%d", &num);
	printf("100=>%d\n", (num / 100) % 10);
	printf("010=>%d\n", (num / 10) % 10);
	printf("001=>%d\n", num % 10);
	return 0;
}

