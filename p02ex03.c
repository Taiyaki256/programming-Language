#include <stdio.h>

int main()
{
	int x, y1, y2, y3, y4;
	printf("   x   y   y2   y3     y4\n");
	for (x = 1; x <= 100; x++)
	{
		y1 = 101 - x;
		y2 = 3 * x;
		y3 = 4 * x - 3;
		y4 = 5 * x * x;
		printf("%4d %4d %4d %4d %6d\n", x, y1, y2, y3, y4);
	}
	return 0;
}

