#include <stdio.h>

int main()
{
	int score[10] = {15, 8, 12, 18, 20, 20, 9, 16, 20, 17};
	int count;
	int i;
	int num;
	for (count = 0; count < 10; count++)
	{
		num = count + 1;
		printf("%2d %2d :", num, score[count]);
		for (i = 1; i <= score[count]; i++)
		{
			if (i % 5 == 0)
			{
				printf("|");
			}
			else
			{
				printf("*");
			}
		}
		printf("\n");
	}

	return 0;
}

